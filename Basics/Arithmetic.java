package Basics;

public class Arithmetic 
{
    public static void main(String[] args) 
    {
        System.out.println(5+3);
        System.out.println(10+3);

        int a=50;
        int b=6;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}